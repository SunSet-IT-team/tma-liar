/**
 * Блок с колодами
 */
export const DecksBlock: FC<DecksProps> = ({ loop = true, onChangeActiveDeck }) => {
  return (
    <div className="content">
      <Swiper
        modules={[Mousewheel]}
        direction={'horizontal'}
        slidesPerView={2.1}
        centeredSlides={true}
        spaceBetween={70}
        touchEventsTarget="container"
        mousewheel={{ forceToAxis: true, releaseOnEdges: true }}
        className="swiper deckSwiper"
        loop={loop}
        onSlideChange={(swiper: SwiperType) => {
          onChangeActiveDeck?.(swiper.realIndex);
        }}
      >
        {testDecks.map((deck, i) => (
          <SwiperSlide key={i} className="swiperSlide deckSwiperSlide">
            <div className="deckSlideItem">
              <img
                src={deck.cover}
                alt={`Deck cover ${i + 1}`}
                className="deckIcon"
                loading="lazy"
                onError={(event) => {
                  event.currentTarget.src = fallbackDeckCover;
                }}
              />
            </div>
          </SwiperSlide>
        ))}
      </Swiper>
    </div>
  );
};
